    private String getUser() {
        int row;
        try {
            row = jTable2.getSelectedRow();
        } catch (Exception e) {
            row = -1;
        }
        return (row == -1) ? "" : modelo.getValueAt(row, 0).toString();
    }

    private void borrarTabla() {
        for (int i = 0; i < modelo.getRowCount();) {
            modelo.removeRow(i);
        }
    }

